                    BufferedReader lea = null;
                        String linea;
                        ruta = new JFileChooser();
                        ruta.showOpenDialog(null);
                        File tab;
                        tab = ruta.getSelectedFile();
                        lea = new BufferedReader( new FileReader(tab) );
